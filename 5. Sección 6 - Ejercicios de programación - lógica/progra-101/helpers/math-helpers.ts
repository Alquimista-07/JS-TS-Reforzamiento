export function addTwoNumbers2( num1: number, num2: number ): number {
    return num1 + num2;
}

export function substractTowNumbers( num1: number, num2: number ): number {
    return num1 - num2;
}

export function timesTwoNumbers( num1: number, num2: number ): number {
    return num1 * num2;
}

export function divideTwoNumbers2( num1: number, num2: number ): number {
    return num1 / num2;
}

